use bits_tailwind_merge::tw_merge;
use dioxus::prelude::*;

fn merge_classes(default: &str, custom: Option<String>) -> String {
    match custom {
        Some(c) => tw_merge!(default, &c),
        None => default.to_string(),
    }
}

